from contextlib import contextmanager
import os
import sys

@contextmanager
def suppress_stdout():
    '''
    From https://stackoverflow.com/a/48205923
    '''
    with open(os.devnull, "w") as devnull:
        old_stdout = sys.stdout
        sys.stdout = devnull
        try:  
            yield
        finally:
            sys.stdout = old_stdout